    fun printHelp() {
        println("help")
    }

    fun type(prompt: String) = runBlocking {
        prompt.forEach() {
            print(it)
            delay(30)
        }
    }

    fun endCredits() = runBlocking {
        type("Written and Directed by: ")
        delay(500)
        type("Bartosz Dźwigała")
        println()
        delay(500)

        type("Produced by: ")
        delay(500)
        type("Bartosz Dźwigała")
        println()
        delay(500)

        type("CAST")
        println()
        delay(500)

        type("Programmer: ")
        delay(500)
        type("Bartosz Dźwigała")
        println()
        delay(500)
        printEnd()
    }
}