                " \\ \\  \\\\|__| \\  \\ \\  \\\\\\  \\ \\  \\/  / / \\ \\  \\ \\  \\_|/__       \\ \\   __  \\ \\  \\  __\\ \\  \\_|/_\\ \\  \\\\ \\  \\   \\ \\  \\  \n" +
